cl.ens.maj = function(mat){

	# ensemble prediction is the majority vote
	pred = c();
	for(i in 1:nrow(mat)){
		t = table(mat[i,]);
		id = which.max(t);
		pred = c(pred, names(id));
	}
	return(pred);

}

