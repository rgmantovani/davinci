library("PMCMR")    
library("ggplot2")
library("reshape2")

##########################################################################################################
##########################################################################################################

setup = function() {

  files = list.files("R", pattern="\\.R$", recursive=TRUE, full.name=TRUE); 
  for(i in files){
    source(i);
  }

  source("../exp/stats.r")

}

##########################################################################################################
##########################################################################################################

main = function() {

  setup()

  data = gettingResults()
  cat("* Data loaded ... \n")
  
  plottingEverything(data = data)
  cat("* Data plotted ... \n")
  
  #just data measures (all-measures)
  doingStatisticalComparisons(data = data)
  cat("* Statistical comparisons done ... \n")

  cat("* Everything done \n")
}

##########################################################################################################
##########################################################################################################

main()

